    private int altaAutobus() {
        AutobusBean autobus=new AutobusBean();
        autobus.setDescripcion(jTextDescripcion.getText());
        autobus.setObservaciones(jTextObservaciones.getText());
        autobus.setPlazasNoEnfermos(Integer.parseInt(jTextNoEnfermos.getText()));
        autobus.setPlazasEnfermos(Integer.parseInt(jTextEnfermos.getText()));
        String filtroViaje = jComboPeregrinaciones.getModel().getElementAt(jComboPeregrinaciones.getSelectedIndex()).split(" - ")[0];
        autobus.setIdViaje(filtroViaje);
        return GestionAutobusesBD.setAutobus(autobus);
    }

    private void mttoAutobus() {
        AutobusBean autobus=new AutobusBean();
        autobus.setIdAutobus(idAutobus);
        autobus.setDescripcion(jTextDescripcion.getText());
        autobus.setObservaciones(jTextObservaciones.getText());
        autobus.setPlazasNoEnfermos(Integer.parseInt(jTextNoEnfermos.getText()));
        autobus.setPlazasEnfermos(Integer.parseInt(jTextEnfermos.getText()));
        String filtroViaje = jComboPeregrinaciones.getModel().getElementAt(jComboPeregrinaciones.getSelectedIndex()).split(" - ")[0];
        autobus.setIdViaje(filtroViaje);
        GestionAutobusesBD.updateAutobus(autobus);
    }
