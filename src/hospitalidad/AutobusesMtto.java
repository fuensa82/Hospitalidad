    private void prepararVentana(){
        if(AutobusesMtto.mtto.equals(this.modoVentana)){
            AutobusBean autobus=GestionAutobusesBD.getDatosAutobus(idAutobus);
            jTextDescripcion.setText(autobus.getDescripcion());
            jTextObservaciones.setText(autobus.getObservaciones());
            jTextEnfermos.setText(""+autobus.getPlazasEnfermos());
            jTextNoEnfermos.setText(""+autobus.getPlazasNoEnfermos());
        }else{
            jTextDescripcion.setText("");
            jTextObservaciones.setText("");
            jTextEnfermos.setText("");
            jTextNoEnfermos.setText("");
        }
        
    }
